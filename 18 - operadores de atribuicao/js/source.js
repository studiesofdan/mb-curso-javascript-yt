// operadores de atribuição
// +=, -=, *=, /=

let numeroUm = 2
let numeroDois = 2

// Sem operador de atribuição
console.log("Sem operador de atribuição: ", numeroUm = numeroUm + numeroDois)

// Atribuindo valores
// Divisão
console.log("Operador de atribuição: Divisão", numeroUm /= numeroDois)

// Multiplicação
console.log("Operador de atribuição: Multiplicação", numeroUm *= numeroDois)

// Adição
console.log("Operador de atribuição: Soma", numeroUm += numeroDois)

// Subtração
console.log("Operador de atribuição: Subtração", numeroUm -= numeroDois)



