// Tipos de dados
// numero
let numero = 7
console.log(numero)
console.log(typeof numero)

// float
let float = 13.7
console.log(float)
console.log(typeof float)

// string
let letraComNumero = '737'
console.log(letraComNumero)
console.log(typeof letraComNumero)

// outros
// Not a Number
console.log(NaN)
console.log(typeof NaN)

// infinity
console.log(typeof + Infinity)
console.log(typeof - Infinity)