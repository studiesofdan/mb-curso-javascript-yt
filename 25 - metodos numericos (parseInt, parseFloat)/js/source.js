// Métodos numéricos
// parseInt
console.log("parseInt")
console.log(parseInt(13.123))
console.log(Number.parseInt(13.123))

// parseFloat
console.log("parseFloat")
console.log(parseFloat(21.212))
console.log(Number.parseFloat(21.212))

// toFixed
console.log("toFixed")
console.log(30.1234567.toFixed(1))

// isNaN
console.log("isNaN")
console.log(isNaN("pass the dutchie"))
console.log(isNaN(13))
console.log(isNaN("21"))

// MAX_VALUE e MIN_VALUE
console.log(Number.MAX_VALUE)
console.log(Number.MIN_VALUE)
